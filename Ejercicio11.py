num1=int(input("Ingrese el primer valor: "))
num2=int(input("Ingresa el segundo valor: "))
if num1>num2:
	suma=num1+num2
	resta=num1-num2
	print("La suma de los numeros es: ")
	print(suma)
	print("La resta de los numeros es: ")
	print(resta)
else:
	producto=num1*num2
	division=num2/num1
	print("El producto de los 2 numeros es: ")
	print(producto)
	print("La division de los 2 numeros es: ")
	print(division)
